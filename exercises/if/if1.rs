//在此函数编写基本的if使用规则以对比两数大小
pub fn bigger(a: i32, b: i32) -> i32 {
    // Complete this function to return the bigger number!
    // Do not use:
    // - another function call
    // - additional variables
    // Execute `rustlings hint if1` for hints
    if (a<b)
    {
        return b;
    }
    else
    {
        return a;
    }
}

